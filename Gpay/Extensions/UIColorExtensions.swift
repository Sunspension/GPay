import UIKit

extension UIColor {
    
    @nonobjc class var mainBlue: UIColor {
        
        return UIColor(red: 55 / 255.0, green: 140 / 255.0, blue: 238 / 255.0, alpha: 1)
    }
    
    @nonobjc class var babyBlueLight: UIColor {
        
        return UIColor(red: 234 / 255.0, green: 243 / 255.0, blue: 254 / 255.0, alpha: 1)
    }
    
    @nonobjc class var babyBlue: UIColor {
        
        return UIColor(red: 219 / 255.0, green: 235 / 255.0, blue: 255 / 255.0, alpha: 1)
    }
    
    @nonobjc class var separatorGray: UIColor {
        
        return UIColor(red: 225 / 255.0, green: 225 / 255.0, blue: 225 / 255.0, alpha: 1)
    }
    
    @nonobjc class var lightYellow: UIColor {
        
